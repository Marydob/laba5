a = {"фамилиия":"Петров","язык":{"английский","китайский"}},
{"фамилиия":"Кузнецов","язык":{"китайский", "русский", "немецкий"}},
{"фамилиия":"Смирнов","язык":{"русский"}}
l = set()
china = []
for st in a:
    l.update(a["язык"])
    if "китайский" in l:
        china.append(st["имя"])
sorted_languages = sorted(l)
print(f"Различные языки, которые знают студенты: {sorted_languages}")
print(f"Студенты, которые знают китайский язык: {', '.join(china)}")
